    private void mayorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mayorBtnActionPerformed
        int mayor = numeros[0];
        int i = 1;
        do{
            System.out.print(numeros[i] + " ");
            if (numeros[i] > mayor){
               mayor = numeros[i];
            }
            i++;
        }while(i < numeros.length);
        String mensaje = "El numero mayor es: " + mayor;
        JOptionPane.showMessageDialog(null, 
                mensaje, 
                "Numero menor", 
                JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_mayorBtnActionPerformed

    private void menorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menorBtnActionPerformed
        int menor = numeros[0];
        int i = 1;
        while (i < numeros.length){
            if (numeros[i] < menor){
                menor = numeros[i];
            }
            i++;
        }
        String mensaje = "El numero menor es: " + menor;
        JOptionPane.showMessageDialog(null, 
                mensaje, 
                "Numero menor", 
                JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_menorBtnActionPerformed

    private void agregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBtnActionPerformed
        try{
            colores.add(String.valueOf(colorTxt.getText()));
            coloresCb.removeAllItems();
            for(String color : colores){
                coloresCb.addItem(color);
            }
            colorTxt.setText("");
        }catch(HeadlessException ex){
            JOptionPane.showMessageDialog(null, 
                "Por favor inserte un color", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregarBtnActionPerformed

